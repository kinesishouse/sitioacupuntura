                        <p>
                            Auriculoterapia
                            En la medicina china la oreja es considerada un órgano con relación hacia todo el cuerpo humano. 
                            La auriculoterapia entonces estimula puntos energéticos situados en la la oreja. La Auriculoterapia 
                            (también llamada Acupuntura de la oreja o Auriculo medicina) consiste básicamente en el tratamiento 
                            de enfermedades estimulando puntos de Acupuntura de la oreja. El cuerpo humano cuenta de diferentes 
                            puntos reflejos (que reflejan la salud de los órganos del cuerpo) dentro de estos se encuentra el ojo,
                            las manos, pies y las orejas. La Auriculoterapia es una rama de la acupuntura además de una técnica
                            complementaria, que se realiza en la oreja del paciente, se basa en el equilibrio de la energía vital. Cuando se presenta la enfermedad existe un desequilibrio de esa energía, estos canales al ser estimulados logran restablecer el equilibrio energético del cuerpo. Esta técnica es utilizada para estimular el tratamiento de diversas zonas y órganos del cuerpo, a través de la ubicación de puntos en la oreja que son estimulados con la colocación
                            de agujas o semillas en la zona auricular obteniendo una mejoría de la dolencia en corto plazo.
                        </p>
                        <p>
                            Moxibustión
                            La Moxibustion es un tratamiento que forma parte de la Medicina Tradicional China.
                            Se utilizan las hojas pulverizadas, secadas y trituradas de la planta Artemisia Vulgaris.
                            O el tabaco de moxa. La moxibustión, al igual que la acupuntura, obtiene sus fundamentos 
                            de la Teoría de los Cinco Elementos. Su objetivo es llevar calor a ciertos puntos ubicados a 
                            lo largo del cuerpo humano. Ayuda a “sacar el calor” de los meridianos del cuerpo y aumenta la 
                            circulación de ki o energía. La moxibustión es ideal para tratar problemas que impliquen debilidad 
                            general,
                            estancamiento y depresión. A través de la Moxibustion se tratan múltiples patologías.
